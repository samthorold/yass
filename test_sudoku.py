from sudoku.sudoku import *


def test_sanity():
    s = (
        "..4 6.. .3."
        ".5. 831 ..9"
        ".13 ... .27"
        ""
        "196 2.. ..3"
        ".4. .9. ..."
        ".3. .57 ..."
        ""
        "... ..5 9.2"
        "56. 9.8 ..."
        "... ... 1.."
    )

    board = parse_board_string(s)

    assert board_to_string(board) == (
        "..4|6..|.3.\n"
        ".5.|831|..9\n"
        ".13|...|.27\n"
        "196|2..|..3\n"
        "---|---|---\n"
        ".4.|.9.|...\n"
        ".3.|.57|...\n"
        "...|..5|9.2\n"
        "---|---|---\n"
        "56.|9.8|...\n"
        "...|...|1..\n"
    )

    assert board_to_string(board, "y") == (
        "111|222|333"
        "111|222|333"
        "111|222|333"
        "444|555|666"
        "---|---|---"
        "444|555|666"
        "444|555|666"
        "777|888|999"
        "---|---|---"
        "777|888|999"
        "777|888|999"
        "777|888|999"
    )

    assert board_to_string(board, "x") == (
        "123|456|789"
        "123|456|789"
        "123|456|789"
        "123|456|789"
        "---|---|---"
        "123|456|789"
        "123|456|789"
        "123|456|789"
        "---|---|---"
        "123|456|789"
        "123|456|789"
    )
