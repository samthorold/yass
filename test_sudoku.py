from sudoku.sudoku import *


def test_sanity():
    s = (
        "..4 6.. .3."
        ".5. 831 ..9"
        ".13 ... .27"
        ""
        "196 2.. ..3"
        ".4. .9. ..."
        ".3. .57 ..."
        ""
        "... ..5 9.2"
        "56. 9.8 ..."
        "... ... 1.."
    )

    board = parse_board_string(s)

    assert board_to_string(board) == (
        "..4|6..|.3.\n"
        ".5.|831|..9\n"
        ".13|...|.27\n"
        "---|---|---\n"
        "196|2..|..3\n"
        ".4.|.9.|...\n"
        ".3.|.57|...\n"
        "---|---|---\n"
        "...|..5|9.2\n"
        "56.|9.8|...\n"
        "...|...|1..\n"
    )

    assert board_to_string(board, "box") == (
        "111|222|333\n"
        "111|222|333\n"
        "111|222|333\n"
        "---|---|---\n"
        "444|555|666\n"
        "444|555|666\n"
        "444|555|666\n"
        "---|---|---\n"
        "777|888|999\n"
        "777|888|999\n"
        "777|888|999\n"
    )

    assert board_to_string(board, "x") == (
        "123|456|789\n"
        "123|456|789\n"
        "123|456|789\n"
        "---|---|---\n"
        "123|456|789\n"
        "123|456|789\n"
        "123|456|789\n"
        "---|---|---\n"
        "123|456|789\n"
        "123|456|789\n"
        "123|456|789\n"
    )

    assert board_to_string(board, "y") == (
        "111|111|111\n"
        "222|222|222\n"
        "333|333|333\n"
        "---|---|---\n"
        "444|444|444\n"
        "555|555|555\n"
        "666|666|666\n"
        "---|---|---\n"
        "777|777|777\n"
        "888|888|888\n"
        "999|999|999\n"
    )
